var opt = {
  data: commonMsg, // 번안 저장 객체 (translate-key > 언어 > 내용)
  language: 'en', // 번안을 원하는 언어
  // target: '.optional', // [선택 사항] 범위 지정을 원할 시 선택자 지정
  cleanDataset: true, // [선택 사항] 치환 후 dataset을 제거할 지 여부 (Default. true)
};

var go = function () {
  GAMEDEX.translate(opt)
    .then(function (result) {
      //console.log(result); // 언어 치환 성공 시 언어 치환 갯수 리턴
    })
    .catch(function (err) {
      //console.log(err); // 언어 치환 실패 시 에러 코드 리턴
    })
    .finally(function () {
      const HTML = document.querySelector('html');
      const BODY = document.querySelector('body');

      HTML.setAttribute('lang', opt.language);
      BODY.classList.add(opt.language);
      BODY.style.visibility = 'visible';

      //타이틀 변수
      let _title = 'Real farm';

      //디스크립션 변수
      let _dec = '';

      //중국어 (간체)
      if (opt.language == 'tc') {
        _title = '';
        _dec = '';
      }
      //아랍어
      else if (opt.language == 'ar') {
        _title = '';
        _dec = '';
        HTML.setAttribute('dir', 'rtl');
      }
      //일본어
      else if (opt.language == 'ja') {
        _title = '';
        _dec = '';
      }

      //데이터 바인딩
      document.title = _title;
      document.querySelector('meta[property="og:title"]').setAttribute('content', _title);
      document.querySelector('meta[property="og:description"]').setAttribute('content', _dec);
      document.querySelector('meta[name="description"]').setAttribute('content', _dec);

      //console.log('end'); // 성공 실패 여부와 상관 없이 실행
    });
};

window.addEventListener('load', function () {
  go();
});
